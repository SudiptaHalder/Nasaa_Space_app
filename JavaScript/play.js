            function loadQuiz() {
                const quizSelect = document.getElementById('quiz-select');
                const selectedQuiz = quizSelect.value;
                const quizTitle = document.getElementById('quiz-title');
                const quizForm = document.getElementById('quiz-form');
                const resultDiv = document.getElementById('result');
            
                resultDiv.innerHTML = ''; // Clear previous results
                quizForm.innerHTML = ''; // Clear previous questions
            
                if (selectedQuiz) {
                    quizTitle.innerText = quizzes[selectedQuiz].title;
                    quizzes[selectedQuiz].questions.forEach((q, index) => {
                        const questionHTML = `
                            <div class="question">
                                <p>${index + 1}. ${q.question}</p>
                                ${q.options.map((option, i) => `
                                    <label class="option">
                                        <input type="radio" name="question${index}" value="${i}">
                                        ${option}
                                    </label>
                                `).join('')}
                            </div>`;
                        quizForm.innerHTML += questionHTML;
                    });
                    document.getElementById('quiz').style.display = 'block';
                } else {
                    quizTitle.innerText = '';
                    document.getElementById('quiz').style.display = 'none';
                }
            }
            
            function submitQuiz() {
                const quizSelect = document.getElementById('quiz-select').value;
                const answers = quizzes[quizSelect].questions.map(q => q.answer);
                let score = 0;
                let resultsHTML = '';
            
                answers.forEach((correctAnswer, index) => {
                    const userAnswer = document.querySelector(`input[name="question${index}"]:checked`);
                    if (userAnswer && parseInt(userAnswer.value) === correctAnswer) {
                        score++;
                    } else {
                        const selectedOption = userAnswer ? userAnswer.nextSibling.nodeValue.trim() : 'No answer selected';
                        const explanation = quizzes[quizSelect].questions[index].explanation;
                        resultsHTML += `
                            <p>Question ${index + 1}: ${quizzes[quizSelect].questions[index].question}<br>
                            Your answer: ${selectedOption}<br>
                            Correct answer: ${quizzes[quizSelect].questions[index].options[correctAnswer]}<br>
                            Explanation: ${explanation}</p>`;
                    }
                });
            
                const resultDiv = document.getElementById('result');
                resultDiv.innerHTML = `You scored ${score} out of ${answers.length}.<br>${resultsHTML}`;
            }